#include <bits/stdc++.h>
#define yes cout << "YES" << endl;
#define no cout << "NO" << endl;
#define ll long long
using namespace std;

int main(){
	ll t; cin >> t;
	while(t--){
        ll n, m; cin >> n >> m;
        for(ll i = 0; i < m; i++){
            ll a, b; cin >> a >> b;
        }
      if(n == m){
          no;
      }
      else{
          yes;
      }
	}
	return 0;
}
